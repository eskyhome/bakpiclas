function result=matmul(A,b)
% ==================================================================
% Description
% ==================================================================
% performes A*b as matirx-vector multiplication

% ==================================================================
% Input
% ==================================================================
%                   ::

% ==================================================================
% Output
% ==================================================================
%                   ::


% ==================================================================
% Function Start
% ==================================================================
%% check dimensions
if ~isvector(b)
  error('not a vector')
end
dim=size(b);dim1=dim(1);dim2=dim(2);
dimension=length(b);
if dim1<dim2
  b=b';
end

%% calculate scalar products
result=zeros(dimension,1);
for i=1:dimension
    result(i)=A(i,:)*b;
end
  


% ==================================================================
% Function End
% ==================================================================
end