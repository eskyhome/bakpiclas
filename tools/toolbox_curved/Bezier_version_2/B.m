function B=B(n,k,x)
% ==================================================================
% Description
% ==================================================================
% evaluates the Bernstein Polynomial B^n_k(x)
% calls function choose(n,k): "als „n über k“, „k aus n“ oder „n tief k“ gesprochen"

% ==================================================================
% Input
% ==================================================================
% n                 :: polynomial oder
% k                 :: index
% x                 :: x_coordinate for 1D polynomial evaluation

% ==================================================================
% Output
% ==================================================================
%                   :: 


% ==================================================================
% Function Start
% ==================================================================
%disp(['n=' num2str(n) ' k=' num2str(k)])
B=(1/(2^n))*choose(n,k)*(x+1).^k.*(1-x).^(n-k);
% ==================================================================
% Function End
% ==================================================================
end
