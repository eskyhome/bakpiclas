function choose=choose(n,k)
% ==================================================================
% Description
% ==================================================================
% "als „n über k“, „k aus n“ oder „n tief k“ gesprochen"

% ==================================================================
% Input
% ==================================================================
%                   :: 

% ==================================================================
% Output
% ==================================================================
%                   :: 


% ==================================================================
% Function Start
% ==================================================================
if or(k==0,n==k)
  choose = 1;
else
  choose = factorial(n) / (factorial(k) * factorial(n-k));
end
% ==================================================================
% Function End
% ==================================================================
end